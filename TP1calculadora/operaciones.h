

/** \brief sumar(float, float); suma dos numeros
 *
 * \param float = primer numero
 * \param float = segundo numero
 * \return float
 *
 */
float sumar(float, float);

/** \brief restar(float, float); hace una resta donde a numeroA se le resta numeroB (numeroA - numeroB)
 *
 * \param float = numeroA
 * \param float = numeroB
 * \return float
 *
 */
float restar(float, float);

/** \brief dividir(float, float); hace una divicion donde numeroA se divide por numeroB (numeroA / numeroB)
 *
 * \param float = numeroA
 * \param float = numeroB
 * \return float
 *
 */
float dividir(float, float);

/** \brief multiplicar(float, float); hace una multiplicacion entre dos numeros
 *
 * \param float = numeroA
 * \param float = numeroB
 * \return float
 *
 */
float multiplicar(float, float);

/** \brief Factorial(float); saca el fatorial de un numero
 *
 * \param float = numero a factoriar
 * \return float
 *
 */
float Factorial(float);

/** \brief resultadoDivicion (char[], float, float, float, char[]);
*          muestra mediante un printf el resultado se mostrara con un mensaje que debe incluir tres %f el primero para
 *         el dividendo, el segundo para el divisor y el tercero para el resultado.
 *         Si el divisor es 0 se mostrara el mensaje de error.
 *
 * \param = mensaje de resultado ej: (el resultado de %f / %f es = %f)
 * \param = el dividendo
 * \param = el divisor
 * \param = el resultado
 * \param = mensaje de error
 * \return
 *
 */
void resultadoDivicion (char[], float, float, float, char[]);
